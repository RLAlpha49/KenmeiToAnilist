/**
 * @packageDocumentation
 * @module theme-mode
 * @description TypeScript type for theme mode options in the application.
 */

/**
 * Represents the theme mode for the application.
 *
 * Possible values:
 * - 'dark': Dark mode
 * - 'light': Light mode
 * - 'system': Follows the system theme
 *
 * @source
 */
export type ThemeMode = "dark" | "light" | "system";
